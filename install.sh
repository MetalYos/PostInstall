#!/bin/bash


# Installing Chromium - Web Browser
sudo pacman -S chromium

# Installing Grub Customizer - GUI for the grub config file
sudo pacman -S grub-customizer

# Installing Okular - Universal document viewer
sudo pacman -S okular

# Installing Meld - File comparison application
sudo pacman -S meld

# Installing KeePassX - Password manager
sudo pacman -S keepassx2

# Installing Inkscape - Professional vector graphics editor
sudo pacman -S inkscape

# Installing Kdenlive - Video editor application
sudo pacman -S kdenlive

# Installing Vim - Vi Improved, a highly configurable, improved version of the vi text editor
sudo pacman -S vim

# Installing Variety - Wallpaper changer that automatically downloads wallpapers
sudo pacman -S variety

# Installing Valgrind - Tool to help find memory-management problems in programs
sudo pacman -S valgrind

# Installing Redshift - Application that changes screen light temperature according to the time
sudo pacman -S redshift

# Installing Visual Studio Code - Advanced Text Editor (and some IDE)
yay -S visual-studio-code-bin
